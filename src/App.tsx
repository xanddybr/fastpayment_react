function Contador(){
  const [count, setCount] = useState(0)

  const aumentar = () => count < 10 && setCount((count) => count + 1);
  
  const diminuir = () => count > 0 && setCount((count) => count - 1);
    
  return(
    <>
     <button 
          type="button"
          className=""
          onClick={aumentar}
        >Aumentar       
    </button>
  <div className="container">Count is {count}</div>
    <button 
          type="button"
          className=""
          onClick={diminuir}
        >Diminuir
    </button>
    </>      
  )
}


